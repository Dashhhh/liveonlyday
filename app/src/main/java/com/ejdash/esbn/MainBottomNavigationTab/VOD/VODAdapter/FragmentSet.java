package com.ejdash.esbn.MainBottomNavigationTab.VOD.VODAdapter;

public class FragmentSet {

    String url;
    String index;
    String user;
    String time;
    String thumbnail;

    public FragmentSet(
            String url,
            String index,
            String user,
            String time,
            String thumbnail
    ) {
        this.url = url;
        this.index = index;
        this.user = user;
        this.time = time;
        this.thumbnail = thumbnail;
    }
}





